/// Enum to use in color generation functions.
/// Allows to generate color values per RGB color channel.
enum RGBComponent {
  /// Corresponds to red(r) color part from (r,g,b).
  red,

  /// Corresponds to green(g) color part from (r,g,b).
  green,

  /// Corresponds to blue(b) color part from (r,g,b).
  blue,
}
